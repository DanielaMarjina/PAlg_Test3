public class Vraja{

    public  String nume;
    public  int persoana;

    public Vraja(String nume,int persoana) {
        this.nume=nume;
        this.persoana=persoana;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public int getPersoana() {
        return persoana;
    }

    public void setPersoana(int persoana) {
        this.persoana = persoana;
    }


}
