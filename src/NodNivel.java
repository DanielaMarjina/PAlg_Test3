public class NodNivel {
    int nod;
    int nivel;

    public NodNivel(int nod, int nivel) {
        this.nod = nod;
        this.nivel = nivel;
    }

    public int getNod() {
        return nod;
    }

    public void setNod(int nod) {
        this.nod = nod;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }
}
